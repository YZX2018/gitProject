package boot.dto;


import boot.annotation.UserUtil;

public class UserDTO {
    @UserUtil
    private String name;
    @UserUtil
    private String age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "UserDTO{" +
                "name='" + name + '\'' +
                ", age='" + age + '\'' +
                '}';
    }

}
